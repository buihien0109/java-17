public class PracticeScanner01 {
    public static void main(String[] args) {
        // Yêu cầu nhập a, b từ bàn phím
        int a = 10;
        int b = 3;

        // Ví dụ sử dụng printf để in ra kết quả
        System.out.printf("%d + %d = %d\n", a, b, a + b);
        System.out.printf("%d - %d = %d\n", a, b, a - b);
        System.out.printf("%d / %d = %.2f\n", a, b, (double) a / b);
    }
}
